public class TypeCastingExample {
    public static void main(String[] args) {
        double num1 = 12.75;
        int num2 = (int) num1;
        System.out.println("Double to int: " + num2);

        int num3 = 8;
        double num4 = (double) num3;
        System.out.println("Int to double: " + num4);
    }
}
